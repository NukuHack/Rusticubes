/// Loads a chunk at the camera's position if not already loaded
pub fn add_def_chunk() {
    unsafe {
        let state = super::get_state();
        let chunk_pos = ChunkCoord::from_world_pos(state.camera_system.camera.position);

        if state.data_system.world.loaded_chunks.contains(&chunk_pos) {
            return;
        }
        if !state.data_system.world.load_chunk(chunk_pos) {
            return;
        }

        if let Some(chunk) = state.data_system.world.get_chunk_mut(chunk_pos) {
            let state_b = super::get_state();
            chunk.make_mesh(state_b.device(), state_b.queue(), true);
        }
    }
}
/// Loads chunks around the camera in a radius
pub fn add_full_world() {
    unsafe {
        let state = super::get_state();
        state
            .data_system
            .world
            .update_loaded_chunks(state.camera_system.camera.position, 6u32);
        let state_b = super::get_state();
        state
            .data_system
            .world
            .make_chunk_meshes(state_b.device(), state_b.queue());
    }
}
/// Improved raycasting function that finds the first non-empty block and its face
pub fn raycast_to_block(camera: &Camera, world: &World, max_distance: f32) -> Option<(Vec3, Vec3)> {
    // Adjust ray origin to block center
    let ray_origin = camera.position;
    let ray_dir = camera.forward();

    // Initialize variables for DDA algorithm
    let step = Vec3::new(
        (ray_dir.x.signum() as i32) as f32,
        (ray_dir.y.signum() as i32) as f32,
        (ray_dir.z.signum() as i32) as f32,
    );

    let mut block_pos = Vec3::new(
        (ray_origin.x.floor() as i32) as f32,
        (ray_origin.y.floor() as i32) as f32,
        (ray_origin.z.floor() as i32) as f32,
    );

    let t_delta = Vec3::new(
        if ray_dir.x != 0.0 {
            1.0 / ray_dir.x.abs()
        } else {
            f32::MAX
        },
        if ray_dir.y != 0.0 {
            1.0 / ray_dir.y.abs()
        } else {
            f32::MAX
        },
        if ray_dir.z != 0.0 {
            1.0 / ray_dir.z.abs()
        } else {
            f32::MAX
        },
    );

    let mut t_max = Vec3::new(
        if step.x > 0.0 {
            (block_pos.x as f32 + 1.0 - ray_origin.x) / ray_dir.x
        } else {
            (block_pos.x as f32 - ray_origin.x) / ray_dir.x
        }
        .abs(),
        if step.y > 0.0 {
            (block_pos.y as f32 + 1.0 - ray_origin.y) / ray_dir.y
        } else {
            (block_pos.y as f32 - ray_origin.y) / ray_dir.y
        }
        .abs(),
        if step.z > 0.0 {
            (block_pos.z as f32 + 1.0 - ray_origin.z) / ray_dir.z
        } else {
            (block_pos.z as f32 - ray_origin.z) / ray_dir.z
        }
        .abs(),
    );

    let mut traveled = 0.0f32;
    let mut normal = Vec3::ZERO;

    while traveled < max_distance {
        // Check current block
        if let Some(block) = world.get_block(block_pos) {
            if !block.is_empty() {
                return Some((block_pos, normal));
            }
        }

        // Move to next block boundary
        if t_max.x < t_max.y && t_max.x < t_max.z {
            normal = Vec3::new(-step.x, 0.0, 0.0);
            block_pos.x += step.x;
            traveled = t_max.x;
            t_max.x += t_delta.x;
        } else if t_max.y < t_max.z {
            normal = Vec3::new(0.0, -step.y, 0.0);
            block_pos.y += step.y;
            traveled = t_max.y;
            t_max.y += t_delta.y;
        } else {
            normal = Vec3::new(0.0, 0.0, -step.z);
            block_pos.z += step.z;
            traveled = t_max.z;
            t_max.z += t_delta.z;
        }
    }

    None
}

/// Places a cube on the face of the block the player is looking at
pub fn place_looked_cube() {
    unsafe {
        let state = super::get_state();
        let camera = &state.camera_system.camera;
        let world = &state.data_system.world;

        if let Some((block_pos, normal)) = raycast_to_block(camera, world, 6.0) {
            let placement_pos = block_pos + normal;

            // Place the cube
            state
                .data_system
                .world
                .set_block(placement_pos, Block::new());

            // Update chunk mesh
            let chunk_pos = ChunkCoord::from_world_pos(placement_pos);
            if let Some(chunk) = state.data_system.world.get_chunk_mut(chunk_pos) {
                let state_b = super::get_state();
                chunk.make_mesh(state_b.device(), state_b.queue(), false);
            }
        }
    }
}

/// Removes the block the player is looking at
pub fn remove_targeted_block() {
    unsafe {
        let state = super::get_state();
        let camera = &state.camera_system.camera;
        let world = &state.data_system.world;

        if let Some((block_pos, _)) = raycast_to_block(camera, world, 10.0) {
            // Set block to air
            state
                .data_system
                .world
                .set_block(block_pos, Block::default());

            // Update chunk mesh
            let chunk_pos = ChunkCoord::from_world_pos(block_pos);
            if let Some(chunk) = state.data_system.world.get_chunk_mut(chunk_pos) {
                let state_b = super::get_state();
                chunk.make_mesh(state_b.device(), state_b.queue(), false);
            }
        }
    }
}

pub struct Texture {
    pub texture: wgpu::Texture,
    pub view: wgpu::TextureView,
    pub sampler: wgpu::Sampler,
}

impl Texture {
    pub fn from_bytes(
        device: &wgpu::Device,
        queue: &wgpu::Queue,
        bytes: &[u8],
        label: &str,
    ) -> std::result::Result<Self, image::ImageError> {
        let img: image::DynamicImage = image::load_from_memory(bytes)?;
        Self::from_image(device, queue, &img, Some(label))
    }

    pub fn from_image(
        device: &wgpu::Device,
        queue: &wgpu::Queue,
        img: &image::DynamicImage,
        label: Option<&str>,
    ) -> std::result::Result<Self, image::ImageError> {
        let rgba: image::RgbaImage = img.to_rgba8();
        let dimensions: (u32, u32) = img.dimensions();

        let size: wgpu::Extent3d = wgpu::Extent3d {
            width: dimensions.0,
            height: dimensions.1,
            depth_or_array_layers: 1,
        };

        let texture: wgpu::Texture = device.create_texture(&wgpu::TextureDescriptor {
            label,
            size,
            mip_level_count: 1,
            sample_count: 1,
            dimension: wgpu::TextureDimension::D2,
            format: wgpu::TextureFormat::Rgba8UnormSrgb,
            usage: wgpu::TextureUsages::TEXTURE_BINDING | wgpu::TextureUsages::COPY_DST,
            view_formats: &[],
        });

        queue.write_texture(
            wgpu::TexelCopyTextureInfo {
                aspect: wgpu::TextureAspect::All,
                texture: &texture,
                mip_level: 0,
                origin: wgpu::Origin3d::ZERO,
            },
            &rgba,
            wgpu::TexelCopyBufferLayout {
                offset: 0,
                bytes_per_row: Some(4 * dimensions.0),
                rows_per_image: Some(dimensions.1),
            },
            size,
        );

        let view: wgpu::TextureView = texture.create_view(&wgpu::TextureViewDescriptor::default());
        let sampler: wgpu::Sampler = device.create_sampler(&wgpu::SamplerDescriptor {
            address_mode_u: wgpu::AddressMode::ClampToEdge,
            address_mode_v: wgpu::AddressMode::ClampToEdge,
            address_mode_w: wgpu::AddressMode::ClampToEdge,
            mag_filter: wgpu::FilterMode::Linear,
            min_filter: wgpu::FilterMode::Nearest,
            mipmap_filter: wgpu::FilterMode::Nearest,
            ..Default::default()
        });

        Ok(Self {
            texture,
            view,
            sampler,
        })
    }

    pub const DEPTH_FORMAT: wgpu::TextureFormat = wgpu::TextureFormat::Depth32Float;

    pub fn create_depth_texture(
        device: &wgpu::Device,
        config: &wgpu::SurfaceConfiguration,
        label: &str,
    ) -> Self {
        let size: wgpu::Extent3d = wgpu::Extent3d {
            width: config.width.max(1),
            height: config.height.max(1),
            depth_or_array_layers: 1,
        };

        let texture: wgpu::Texture = device.create_texture(&wgpu::TextureDescriptor {
            label: Some(label),
            size,
            mip_level_count: 1,
            sample_count: 1,
            dimension: wgpu::TextureDimension::D2,
            format: Self::DEPTH_FORMAT,
            usage: wgpu::TextureUsages::RENDER_ATTACHMENT | wgpu::TextureUsages::TEXTURE_BINDING,
            view_formats: &[],
        });

        let view: wgpu::TextureView = texture.create_view(&wgpu::TextureViewDescriptor::default());

        Self {
            texture,
            view,
            sampler: device.create_sampler(&wgpu::SamplerDescriptor {
                address_mode_u: wgpu::AddressMode::ClampToEdge,
                address_mode_v: wgpu::AddressMode::ClampToEdge,
                compare: Some(wgpu::CompareFunction::LessEqual),
                ..Default::default()
            }),
        }
    }
}

// --- Texture Manager ---
pub struct TextureManager {
    pub texture: Texture,
    pub depth_texture: Texture,
    pub bind_group: wgpu::BindGroup,
    pub bind_group_layout: wgpu::BindGroupLayout,
    // ... existing fields ...
    pub render_texture: wgpu::Texture,
    pub render_texture_view: wgpu::TextureView,
    pub post_processing_bind_group: wgpu::BindGroup,
}

impl TextureManager {
    pub fn new(
        device: &wgpu::Device,
        queue: &wgpu::Queue,
        config: &wgpu::SurfaceConfiguration,
    ) -> Self {
        let texture_path = "resources/cube-diffuse.jpg";
        let bytes = std::fs::read(texture_path).expect("Texture not found");
        let texture = Texture::from_bytes(device, queue, &bytes, texture_path).unwrap();

        let depth_texture = Texture::create_depth_texture(device, config, "Depth Texture");

        let bind_group_layout = device.create_bind_group_layout(&wgpu::BindGroupLayoutDescriptor {
            entries: &[
                wgpu::BindGroupLayoutEntry {
                    binding: 0,
                    visibility: wgpu::ShaderStages::FRAGMENT,
                    ty: wgpu::BindingType::Texture {
                        multisampled: false,
                        view_dimension: wgpu::TextureViewDimension::D2,
                        sample_type: wgpu::TextureSampleType::Float { filterable: true },
                    },
                    count: None,
                },
                wgpu::BindGroupLayoutEntry {
                    binding: 1,
                    visibility: wgpu::ShaderStages::FRAGMENT,
                    ty: wgpu::BindingType::Sampler(wgpu::SamplerBindingType::Filtering),
                    count: None,
                },
            ],
            label: Some("Texture Bind Group Layout"),
        });

        let bind_group = device.create_bind_group(&wgpu::BindGroupDescriptor {
            layout: &bind_group_layout,
            entries: &[
                wgpu::BindGroupEntry {
                    binding: 0,
                    resource: wgpu::BindingResource::TextureView(&texture.view),
                },
                wgpu::BindGroupEntry {
                    binding: 1,
                    resource: wgpu::BindingResource::Sampler(&texture.sampler),
                },
            ],
            label: Some("Texture Bind Group"),
        });

        // Create render texture for FXAA
        let render_texture = device.create_texture(&wgpu::TextureDescriptor {
            label: Some("Render Texture"),
            size: wgpu::Extent3d {
                width: config.width,
                height: config.height,
                depth_or_array_layers: 1,
            },
            mip_level_count: 1,
            sample_count: 1,
            dimension: wgpu::TextureDimension::D2,
            format: config.format,
            usage: wgpu::TextureUsages::RENDER_ATTACHMENT | wgpu::TextureUsages::TEXTURE_BINDING,
            view_formats: &[],
        });

        let render_texture_view =
            render_texture.create_view(&wgpu::TextureViewDescriptor::default());

        // Create bind group layout for post processing
        let post_processing_bind_group_layout =
            device.create_bind_group_layout(&wgpu::BindGroupLayoutDescriptor {
                label: Some("Post Processing Bind Group Layout"),
                entries: &[
                    wgpu::BindGroupLayoutEntry {
                        binding: 0,
                        visibility: wgpu::ShaderStages::FRAGMENT,
                        ty: wgpu::BindingType::Texture {
                            sample_type: wgpu::TextureSampleType::Float { filterable: true },
                            view_dimension: wgpu::TextureViewDimension::D2,
                            multisampled: false,
                        },
                        count: None,
                    },
                    wgpu::BindGroupLayoutEntry {
                        binding: 1,
                        visibility: wgpu::ShaderStages::FRAGMENT,
                        ty: wgpu::BindingType::Sampler(wgpu::SamplerBindingType::Filtering),
                        count: None,
                    },
                ],
            });

        // Create sampler for post processing
        let post_sampler = device.create_sampler(&wgpu::SamplerDescriptor {
            label: Some("Post Processing Sampler"),
            address_mode_u: wgpu::AddressMode::ClampToEdge,
            address_mode_v: wgpu::AddressMode::ClampToEdge,
            address_mode_w: wgpu::AddressMode::ClampToEdge,
            mag_filter: wgpu::FilterMode::Linear,
            min_filter: wgpu::FilterMode::Linear,
            mipmap_filter: wgpu::FilterMode::Linear,
            ..Default::default()
        });

        // Create bind group for post processing
        let post_processing_bind_group = device.create_bind_group(&wgpu::BindGroupDescriptor {
            label: Some("Post Processing Bind Group"),
            layout: &post_processing_bind_group_layout,
            entries: &[
                wgpu::BindGroupEntry {
                    binding: 0,
                    resource: wgpu::BindingResource::TextureView(&render_texture_view),
                },
                wgpu::BindGroupEntry {
                    binding: 1,
                    resource: wgpu::BindingResource::Sampler(&post_sampler),
                },
            ],
        });

        Self {
            texture,
            depth_texture,
            bind_group,
            bind_group_layout,
            // ... existing fields ...
            render_texture,
            render_texture_view,
            post_processing_bind_group,
        }
    }
}

